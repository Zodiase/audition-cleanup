var path = require("path"),
    async = require("async"),
    argv = require('yargs')
    .usage('Usage: $0 <directory>[ <directory>]')
    .string('_')
    .demand(1)
    .argv;

// These modules are generated by `npm run build`.
var thenErrorHelper = require('../lib/thenErrorHelper.js').thenErrorHelper,
    cleanupProjectFolder = require('../lib/audition-cleanup.js').cleanupProjectFolder;

var inputDirs = (Array.isArray(argv._) ? argv._ : [argv._]).map(path.normalize);

async.eachSeries(inputDirs, function cleanupThisProject (projectDir, next) {

  console.log('Checking directory: ' + path.resolve(projectDir));

  cleanupProjectFolder(projectDir, function (err, filesDeleted) {

    if (thenErrorHelper(err, next)) return;

    console.log('Files deleted:', filesDeleted);

    next(null);

  });

}, function cleanupAllComplete (err) {

  if (thenErrorHelper(err)) return;

  console.log('Cleanup complete.');

});
